#splitting function: https://sqlpey.com/python/python-string-split-keep-delimiters/
def split_string_with_separators(regex_pattern, text_content):
   # Compile the regex pattern for efficiency
   separator_regex = re.compile(regex_pattern)

   # Split the string using the regex
   split_parts = separator_regex.split(text_content)

   resulting_list = []
   current_pos = 0
   for part in split_parts:
     if not part: continue # Skip empty parts if any

     # Find the position of the current part in the original string
     found_pos = text_content.find(part, current_pos)

     # If there's content before the current part, and it's not the same as the last appended item, add it
     if found_pos > current_pos and (not resulting_list or resulting_list[-1] != text_content[current_pos:found_pos]):
       resulting_list.append(text_content[current_pos:found_pos])

     resulting_list.append(part)
     current_pos = found_pos + len(part)

   # Add any remaining content at the end
   if current_pos < len(text_content):
     resulting_list.append(text_content[current_pos:])

   return resulting_list

