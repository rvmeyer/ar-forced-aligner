def bw_to_caphi (word):
  word = word.strip('\n')
  analysis = analyzer.analyze(word)
  if len(analysis) > 0:
    for i in range(len(analysis)):
      if analysis[i]['pos'] not in ['foreign', 'digit', 'punc']:
        sent_bw = ar2bw(analysis[i]['diac'])
        new_value = []
        for letter in sent_bw:
          new_value.append(letter)
        new_value = '_'.join(new_value) #need to join and then resplit-all bw transcriptions are one letter, but caphi uses multichar transcriptions
        new_value = new_value.split('_')
        for index, letter in enumerate(new_value):
          for bw in range(len(correspondences)):
            if correspondences[bw][0] == letter:
              new_value[index] = correspondences[bw][1]
          if letter in special_chars:
            if letter == 'Y':
              new_value_copy = new_value
              new_value[index] = 'y'
              new_value_copy[index] = 'aa'
            elif letter == 'A':
              if len(new_value) == 1 or new_value[-2] == 'a':
                new_value[index] = 'a'
              else:
                new_value[index] = 'aa'
            elif letter == '~':
              if new_value[index-2] == 'l': #deals with sheddah indicating coronal assimilation
                new_value[index-2] = new_value[index-1]
                new_value[index] = ''
              else:
                new_value[index] = new_value[index-1]
